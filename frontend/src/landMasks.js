export function isApproximateLandCell(locationId, lat, lon) {
  const mask = LAND_MASKS[locationId];
  if (!mask) {
    return true;
  }

  const insideIncludedLand = !mask.include?.length || mask.include.some((polygon) => pointInPolygon(lat, lon, polygon));
  if (!insideIncludedLand) {
    return false;
  }

  return !(mask.exclude || []).some((polygon) => pointInPolygon(lat, lon, polygon));
}

function pointInPolygon(lat, lon, polygon) {
  let inside = false;
  for (let current = 0, previous = polygon.length - 1; current < polygon.length; previous = current, current += 1) {
    const [currentLat, currentLon] = polygon[current];
    const [previousLat, previousLon] = polygon[previous];
    const crossesLatitude = currentLat > lat !== previousLat > lat;
    if (!crossesLatitude) {
      continue;
    }
    const intersectLon = ((previousLon - currentLon) * (lat - currentLat)) / (previousLat - currentLat) + currentLon;
    if (lon < intersectLon) {
      inside = !inside;
    }
  }
  return inside;
}
